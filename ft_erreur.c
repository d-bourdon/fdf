#include "fdf.h"

void	ft_erreur(char *s, int mode)
{
	if (mode == 1)
	{
		ft_putstr("\033[1;41mErreur\033[40m : ");
		ft_putstr(s);
		ft_putstr("\n\033[00m");
		exit(1);
	}
}
